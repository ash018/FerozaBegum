                        <h4>Feroza Begum</h4>
                        <p>Nazrul Sangeet legend Firoza Begum was born in a Muslim family in Faridpur, Bangladesh on 28 July 1930 to the Zamindars of Ratail Ghonaparha. She became drawn to music in her childhood. She started her career in 1940s.
							She first sang in All India Radio while studying in sixth grade. She met poet Kazi Nazrul Islam at the age of 10. In 1942, she recorded her first Islamic song by the gramophone record company HMV in 78 rpm disk format. Since then, 12 LP, 4 EP, 6 CD and more than 20 audio cassette records have been released. She lived in Kolkata from 1954 until she moved to Dhaka in 1967. 
						</p>
						<p>	In 1956, Firoza Begum was married to Kamal Das Gupta, a singer, composer and lyricist. Kamal died on 20 July 1974. Her sons Hamin Ahmed and Shafin Ahmed are also musicians. They are currently members of the rock band Miles.<br></p>
							<p>Firoza Begum died on 9 September 2014 around 8:28 pm in Apollo Hospital, Dhaka at the age of 84 due to heart and kidney problems.
						</p>	
